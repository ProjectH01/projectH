package com.projectH.model;

public class CompanycashBean {

	private String company_id;
	private int char_cash;
	private String char_date;
	private int take_price;
	private String take_date;
	
	public String getCompany_id() {
		return company_id;
	}
	public void setCompany_id(String company_id) {
		this.company_id = company_id;
	}
	public int getChar_cash() {
		return char_cash;
	}
	public void setChar_cash(int char_cash) {
		this.char_cash = char_cash;
	}
	public String getChar_date() {
		return char_date;
	}
	public void setChar_date(String char_date) {
		this.char_date = char_date;
	}
	public int getTake_price() {
		return take_price;
	}
	public void setTake_price(int take_price) {
		this.take_price = take_price;
	}
	public String getTake_date() {
		return take_date;
	}
	public void setTake_date(String take_date) {
		this.take_date = take_date;
	}
	
	
}
